#Principles and objects orientation:- 
''' Object oriented programming(OOP) is based on several fundamental principles and concepts
that guide the design and organisation of software. These principles help in creating more
manageble, modular, and maintainable code.'''
''' Abstraction:- Abstraction is the process of simplifying complex reality by modeling classes
based on real-world entities. It involves identifying the essential characteristics and 
behaviour of an object while ignoring the non-essential details. '''
''' Inheritance:- Inheritance allows you to create a new class by reusing the attributes and
methods of an existing class. The new class is referred to as a subclass or derived class, and
the existing class is the superclass or base class. '''
''' Polymorphism:- Polymorphism allows objects of different classes to be treated as objects of 
a common superclass. It enables objects to respond to method calls based on their specific
class implementations. '''
''' Encapulation:- Practice of binding data and method that operate on data into a single 
unit. '''
''' File Organisation:- File organisation refers to the way data and information are 
structured and stored within files or directories in a computer or information system. '''
'''Types of File Organisation:-
1. Sequential File Organisation
2. Indexed File Organisation
3. Relative File Organisation
4. Random Access File Organisation '''

